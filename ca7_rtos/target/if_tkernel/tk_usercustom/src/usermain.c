static void OS_User_Ini(void)
{
	INT		i;
	ID		id;
	static const CHAR	task_name_null[] = "";
	INT		val[1];
	
	// Sem
	i = 0;
	for ( ;; ) {
		if ( gOS_Sem_Tbl[i].idx == -1 ) {
			break;
		}
		id = tk_cre_sem( (T_CSEM*)&gOS_Sem_Tbl[i].csem );
		if ( id < 0 ) {
			while (1);
		}
		gSemID[gOS_Sem_Tbl[i].idx] = id;
		i++;
	}
	
	// Flag
	i = 0;
	for ( ;; ) {
		if ( gOS_Flag_Tbl[i].idx == -1 ) {
			break;
		}
		id = tk_cre_flg( (T_CFLG*)&gOS_Flag_Tbl[i].cflg );
		if ( id < 0 ) {
			while (1);
		}
		gFlagID[gOS_Flag_Tbl[i].idx] = id;
		i++;
	}
	
	// Mtx
	i = 0;
	for ( ;; ) {
		if ( gOS_Mtx_Tbl[i].idx == -1 ) {
			break;
		}
		id = tk_cre_mtx( (T_CMTX*)&gOS_Mtx_Tbl[i].cmtx );
		if ( id < 0 ) {
			while (1);
		}
		gMtxID[gOS_Mtx_Tbl[i].idx] = id;
		i++;
	}
	
	// Variable size memory pool
	i = 0;
	for ( ;; ) {
		if ( gOS_MemPool_Tbl[i].idx == -1 ) {
			break;
		}
		id = tk_cre_mpl( (T_CMPL*)&gOS_MemPool_Tbl[i].cmpl );
		if ( id < 0 ) {
			while (1);
		}
		gMemPoolID[gOS_MemPool_Tbl[i].idx] = id;
		i++;
	}
	
	// Fixed memory pool
	i = 0;
	for ( ;; ) {
		if ( gOS_MemPoolFixed_Tbl[i].idx == -1 ) {
			break;
		}
		id = tk_cre_mpf( (T_CMPF*)&gOS_MemPoolFixed_Tbl[i].cmpf );
		if ( id < 0 ) {
			while (1);
		}
		gMemPoolFixedID[gOS_MemPoolFixed_Tbl[i].idx] = id;
		i++;
	}
	
	// Mailboxes
	i = 0;
	for ( ;; ) {
		if ( gOS_MailBox_Tbl[i].idx == -1 ) {
			break;
		}
		id = tk_cre_mbx( (T_CMBX*)&gOS_MailBox_Tbl[i].cmbx );
		if ( id < 0 ) {
			while (1);
		}
		gMailBoxID[gOS_MailBox_Tbl[i].idx] = id;
		i++;
	}

	// TSK
	T_CTSK		wk_ctsk;
	
	i = 0;
	tk_get_cfn(MPTK_CFG_TKERPRNUM, val, 1);
	for ( ;; ) {
		if ( gOS_Tsk_Tbl[i].idx == -1 ) {
			break;
		}
		gTaskCreated[gOS_Tsk_Tbl[i].idx] = 0;
		gTaskName[gOS_Tsk_Tbl[i].idx] = task_name_null;
		
		memcpy(&wk_ctsk, &gOS_Tsk_Tbl[i].ctsk, sizeof(wk_ctsk));
		if ( val[0] == 1 ) {
			wk_ctsk.assprc = TP_PRC1;
		}
//		id = tk_cre_tsk( (T_CTSK*)&gOS_Tsk_Tbl[i].ctsk );
		id = tk_cre_tsk( (T_CTSK*)&wk_ctsk );
		if ( id < 0 ) {
			while (1);
		}
		gTaskID[gOS_Tsk_Tbl[i].idx] = id;
		gTaskName[gOS_Tsk_Tbl[i].idx] = gOS_Tsk_Tbl[i].name;
		gTaskCreated[gOS_Tsk_Tbl[i].idx] = 1;
		i++;
	}

}

INT32 OS_User_Get_MbaTidFromTid( const OS_USER_ID tid )
{
	UINT32	loopcnt;

	for( loopcnt = 0; loopcnt < TID_NUM; loopcnt++ ) {
		if( gTaskCreated[loopcnt] == 0 ) {
			continue;
		}
		if( tid == gTaskID[loopcnt] ) {
			return loopcnt;
		}
	}

	return -1;	// Unknown
}

BOOL OS_User_Get_TidFromMbaTid( OS_USER_ID* const p_tid, const INT32 mba_tid )
{
	if( gTaskCreated[mba_tid] == 0 ) {
		return FALSE;
	}

	*p_tid = gTaskID[mba_tid];

	return TRUE;
}

VOID* OS_User_Get_TaskMainAddr( const OS_USER_ID tid )
{
	UINT32	num;
	UINT32	i;

	num = sizeof(gOS_Tsk_Tbl) / sizeof(T_OS_USR_CTSK);
	for ( i = 0; i < num; i++ ) {
		if( gTaskCreated[gOS_Tsk_Tbl[i].idx] == 0 ) {
			continue;
		}
		if( gTaskID[gOS_Tsk_Tbl[i].idx] == tid ) {
			return gOS_Tsk_Tbl[i].ctsk.task;
		}
	}

	return NULL;
}


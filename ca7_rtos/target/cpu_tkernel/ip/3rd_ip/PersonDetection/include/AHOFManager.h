/**
  Contains AHOF manager configuration used when calling AHOF_LIB_VFT::CreateManager().
@ingroup AHOFAPI_Mgr
**************************************************************************/
typedef struct AHOF_MANAGER_CFG
{
    /** The size, in bytes, of this structure. Set to <i>sizeof(AHOF_MANAGER_CFG)</i>
      before calling any function that uses this structure.
    */
    UINT32      structSize;
} AHOF_MANAGER_CFG;

///////////////////////////////////////////////////////////////////////////
// AHOF_PREVIEW_FLAGS
/**
  These flags control how the preview stream is generated.
@ingroup AHOFAPI_Mgr
**************************************************************************/
enum AHOF_PREVIEW_FLAGS
{
    /// If set, enables the high resolution luminance map.
    /// @hideinitializer
    AHOF_PREVIEW_MAP_Y_HIRES = (1 << 0),
    /// If set, enables the YUV420P map, else only luminance will be available.
    /// @hideinitializer
    AHOF_PREVIEW_MAP_YUV     = (1 << 1)
};

///////////////////////////////////////////////////////////////////////////
// AHOF_PREVIEW_CFG
/**
  Contains AHOF manager configuration used when calling AHOF_MANAGER_VFT::StartPreview().
@ingroup AHOFAPI_Mgr
**************************************************************************/
typedef struct AHOF_PREVIEW_CFG
{
    /** The size, in bytes, of this structure. Set to <i>sizeof(AHOF_PREVIEW_CFG)</i>
      before calling any function that uses this structure.
    */
    UINT32      structSize;

    /** The flags that control what maps should be enabled.
      @sa AHOF_PREVIEW_FLAGS
    */
    UINT32      flags;

    /** The crop applied to the camera stream. */
    GT_RECT     crop;

    /** The size of the maps. */
    GT_SIZE     size;

    /** The size of the High Resolution maps. */
    GT_SIZE     sizeHighRes;

    /** The buffer that will be used for PRE maps. */
    void*       buffer;

    /** The size of the buffer used for PRE maps. */
    UINT32      bufferSize;
} AHOF_PREVIEW_CFG;


///////////////////////////////////////////////////////////////////////////
// AHOF_IMAGE_METADATA
/**
  Contains additional information related to a image.
@ingroup AHOFAPI_Mgr
**************************************************************************/
typedef struct AHOF_IMAGE_METADATA
{
    /** Pointer to the next metadata node */
    struct AHOF_IMAGE_METADATA* next;
    /** The type of the metadata node.
      @sa AHOF_IMAGE_METADATA_TYPE
    */
    UINT32                      type;
} AHOF_IMAGE_METADATA;

///////////////////////////////////////////////////////////////////////////
// AHOF_IMAGE_METADATA_TYPE
/**
  The known types of image metadata.
@ingroup AHOFAPI_Mgr
**************************************************************************/
enum AHOF_IMAGE_METADATA_TYPE
{
    /// The metadata represent maps generated by PRE module.
    /// @hideinitializer
    AHOF_IMAGE_METADATA_TYPE_PRE_MAPS   = 0,

    /// The metadata represent a scaled (smaller or bigger)
    /// version of the image.
    /// @hideinitializer
    AHOF_IMAGE_METADATA_TYPE_SCALED     = 1
};

///////////////////////////////////////////////////////////////////////////
// AHOF_IMAGE_METADATA_PRE_MAPS
/**
  Contains maps and information generated by the PRE module.
@ingroup AHOFAPI_Mgr
*/
typedef struct AHOF_IMAGE_METADATA_PRE_MAPS
{
    /** Pointer to the next metadata node */
    AHOF_IMAGE_METADATA*        next;

    /** The type of the metadata node.
      @sa AHOF_IMAGE_METADATA_TYPE
    */
    UINT32                      type;

    /** The pointer to the 32 bit Integral Image
      for the Y channel. (OPTIONAL)
    */
    UINT32*                     II;

    /** The pointer to the 32 bit Squared Integral Image
      for the Y channel. (OPTIONAL)
    */
    UINT32*                     II2;

    /** Pointer to the skin-map. (OPTIONAL)
    */
    UINT8*                      skin;

    /** Pointer to the skin-map Integral Image. (OPTIONAL)
    */
    UINT32*                     skin_II;

    /** Pointer to the image histogram. (OPTIONAL)
      If image contains more channels, all histograms
      are available one after each other.
    */
    UINT32*                     hist;

    /** The saturation of the image.
    */
    UINT32                      sat;
} AHOF_IMAGE_METADATA_PRE_MAPS;

///////////////////////////////////////////////////////////////////////////
// AHOF_IMAGE_METADATA_SCALED
/**
  Contains information regarding a scaled version of the image.
@ingroup AHOFAPI_Mgr
*/
typedef struct AHOF_IMAGE_METADATA_SCALED
{
    /** Pointer to the next metadata node */
    AHOF_IMAGE_METADATA*        next;

    /** The type of the metadata node.
      @sa AHOF_IMAGE_METADATA_TYPE
    */
    UINT32                      type;

    /** Pointer to a scaled version of the original image.
    */
    GT_IMAGE*                   img;

    /** Metadata associated with the scaled image.
    */
    AHOF_IMAGE_METADATA*        metadata;
} AHOF_IMAGE_METADATA_SCALED;

///////////////////////////////////////////////////////////////////////////
// AHOF_MANAGER_RESULTS
/**
  Contains AHOF manager results for an image.
@ingroup AHOFAPI_Mgr
**************************************************************************/
typedef struct AHOF_MANAGER_RESULTS
{
    /** The size, in bytes, of this structure. Set to <i>sizeof(AHOF_MANAGER_RESULTS)</i>
      before calling any function that uses this structure.
    */
    UINT32                      structSize;

    /** The bit mask for the detectors that generated the results
    */
    UINT32                      detBitmask;

    /** The image associated with the results.
    */
    const GT_IMAGE*             img;

    /** The metadata associated with the image.
    */
    const AHOF_IMAGE_METADATA*  imgMeta;

    /** Array that contains the number of detections for each detection array (@a detArray).
    @note
      The order of the detections is given by detBitmask (detector with bit mask 1,
      will be first, then the one with bit mask 2, then 4...)
    */
    UINT32*                     nbDetections;

    /** Array that contains pointers to the detection array generated by each detector.
    */
    struct AHOF_DETECTION**     detArray;
} AHOF_MANAGER_RESULTS;

///////////////////////////////////////////////////////////////////////////
